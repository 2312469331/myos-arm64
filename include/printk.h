#ifndef __PRINTK_H__
#define __PRINTK_H__

#include <stddef.h>

// ============================================================
// printk 格式化规范
// ============================================================
// 
// 格式说明符格式：%[标志][宽度][长度]类型
// 
// 【标志】
//  - '-': 左对齐（默认右对齐）
//  - '+': 显示正负号（仅对有符号类型）
//  - '0': 零填充（在宽度内用0填充）
//  - '#': 替代格式（添加进制前缀）
// 
// 【宽度】
//  - 数字: 最小输出宽度，不足时填充空格或0
// 
// 【长度修饰符】
//  - 'l': long（32位系统32位，64位系统64位）
//  - 'll': long long（64位）
// 
// 【类型】
//  - 's': 字符串
//  - 'c': 字符
//  - 'd'/'i': 有符号十进制
//  - 'u': 无符号十进制
//  - 'x': 小写十六进制（无前缀，除非使用 #）
//  - 'X': 大写十六进制（无前缀，除非使用 #）
//  - 'o': 八进制（无前缀，除非使用 #）
//  - 'b': 二进制（无前缀，除非使用 #）
//  - 'p': 指针地址（自动带 0x 前缀，默认宽度16）
//  - 'zu': size_t 无符号十进制
//  - 'zx': size_t 十六进制
//  - '%': 打印百分号
// 
// 【进制前缀规则】
//  - 默认无前缀
//  - 使用 # 标志时添加前缀：
//    * 十六进制: 0x / 0X（取决于大小写）
//    * 八进制: 0
//    * 二进制: 0b
// 
// ============================================================

// 格式化打印
void printk(const char *fmt, ...);

// 致命错误处理：打印信息后死循环
void panic(const char *fmt, ...);

#endif // __PRINTK_H__